namespace ChessGame
{
    /// <summary>
    /// Piece-Square Tables (PST) — White perspective, row 0 = rank 8, row 7 = rank 1.
    /// Values in centipawns. Inspired by Stockfish tuning.
    /// </summary>
    public static class PieceTables
    {
        public static readonly int[,] Pawn = {
            {  0,  0,  0,  0,  0,  0,  0,  0 },
            { 50, 50, 50, 50, 50, 50, 50, 50 },
            { 10, 10, 20, 30, 30, 20, 10, 10 },
            {  5,  5, 10, 25, 25, 10,  5,  5 },
            {  0,  0,  0, 20, 20,  0,  0,  0 },
            {  5, -5,-10,  0,  0,-10, -5,  5 },
            {  5, 10, 10,-20,-20, 10, 10,  5 },
            {  0,  0,  0,  0,  0,  0,  0,  0 }
        };

        public static readonly int[,] Knight = {
            {-50,-40,-30,-30,-30,-30,-40,-50 },
            {-40,-20,  0,  0,  0,  0,-20,-40 },
            {-30,  0, 10, 15, 15, 10,  0,-30 },
            {-30,  5, 15, 20, 20, 15,  5,-30 },
            {-30,  0, 15, 20, 20, 15,  0,-30 },
            {-30,  5, 10, 15, 15, 10,  5,-30 },
            {-40,-20,  0,  5,  5,  0,-20,-40 },
            {-50,-40,-30,-30,-30,-30,-40,-50 }
        };

        public static readonly int[,] Bishop = {
            {-20,-10,-10,-10,-10,-10,-10,-20 },
            {-10,  0,  0,  0,  0,  0,  0,-10 },
            {-10,  0,  5, 10, 10,  5,  0,-10 },
            {-10,  5,  5, 10, 10,  5,  5,-10 },
            {-10,  0, 10, 10, 10, 10,  0,-10 },
            {-10, 10, 10, 10, 10, 10, 10,-10 },
            {-10,  5,  0,  0,  0,  0,  5,-10 },
            {-20,-10,-10,-10,-10,-10,-10,-20 }
        };

        public static readonly int[,] Rook = {
            {  0,  0,  0,  0,  0,  0,  0,  0 },
            {  5, 10, 10, 10, 10, 10, 10,  5 },
            { -5,  0,  0,  0,  0,  0,  0, -5 },
            { -5,  0,  0,  0,  0,  0,  0, -5 },
            { -5,  0,  0,  0,  0,  0,  0, -5 },
            { -5,  0,  0,  0,  0,  0,  0, -5 },
            { -5,  0,  0,  0,  0,  0,  0, -5 },
            {  0,  0,  0,  5,  5,  0,  0,  0 }
        };

        public static readonly int[,] Queen = {
            {-20,-10,-10, -5, -5,-10,-10,-20 },
            {-10,  0,  0,  0,  0,  0,  0,-10 },
            {-10,  0,  5,  5,  5,  5,  0,-10 },
            { -5,  0,  5,  5,  5,  5,  0, -5 },
            {  0,  0,  5,  5,  5,  5,  0, -5 },
            {-10,  5,  5,  5,  5,  5,  0,-10 },
            {-10,  0,  5,  0,  0,  0,  0,-10 },
            {-20,-10,-10, -5, -5,-10,-10,-20 }
        };

        public static readonly int[,] KingMiddleGame = {
            {-30,-40,-40,-50,-50,-40,-40,-30 },
            {-30,-40,-40,-50,-50,-40,-40,-30 },
            {-30,-40,-40,-50,-50,-40,-40,-30 },
            {-30,-40,-40,-50,-50,-40,-40,-30 },
            {-20,-30,-30,-40,-40,-30,-30,-20 },
            {-10,-20,-20,-20,-20,-20,-20,-10 },
            { 20, 20,  0,  0,  0,  0, 20, 20 },
            { 20, 30, 10,  0,  0, 10, 30, 20 }
        };

        public static readonly int[,] KingEndGame = {
            {-50,-40,-30,-20,-20,-30,-40,-50 },
            {-30,-20,-10,  0,  0,-10,-20,-30 },
            {-30,-10, 20, 30, 30, 20,-10,-30 },
            {-30,-10, 30, 40, 40, 30,-10,-30 },
            {-30,-10, 30, 40, 40, 30,-10,-30 },
            {-30,-10, 20, 30, 30, 20,-10,-30 },
            {-30,-30,  0,  0,  0,  0,-30,-30 },
            {-50,-30,-30,-30,-30,-30,-30,-50 }
        };

        public static int GetPST(PieceType type, PieceColor color, int row, int col, bool endGame = false)
        {
            // Mirror for black
            int r = color == PieceColor.White ? row : 7 - row;
            int c = col;
            return type switch
            {
                PieceType.Pawn   => Pawn[r, c],
                PieceType.Knight => Knight[r, c],
                PieceType.Bishop => Bishop[r, c],
                PieceType.Rook   => Rook[r, c],
                PieceType.Queen  => Queen[r, c],
                PieceType.King   => endGame ? KingEndGame[r, c] : KingMiddleGame[r, c],
                _ => 0
            };
        }
    }
}
